    /**
     * 初始化websocket连接
     */
    private void initSocketClient() {
        URI uri = URI.create(Util.ws);
        HashMap<String,String> temp = new HashMap<>();
        temp.put("Authorization", MyApplication.getInstance().getPersonalInf().getToken());
        client = new JWebSocketClient(uri,temp) {
            @Override
            public void onMessage(String message) {
                System.out.println("JWebSocketClientService收到的消息：" + message);
                Intent intent = new Intent();
                intent.setAction("com.feiyueve.snsdemo.content");
                intent.putExtra("message", message);
                sendBroadcast(intent);
                //checkLockAndShowNotification(message);
            }

            @Override
            public void onOpen(ServerHandshake handshakedata) {
                super.onOpen(handshakedata);
                System.out.println("JWebSocketClientService websocket连接成功");
            }
        };
        connect();
    }

    /**
     * 连接websocket
     */
    private void connect() {
        new Thread() {
            @Override
            public void run() {
                try {
                    //connectBlocking多出一个等待操作，会先连接再发送，否则未连接发送会报错
                    client.connectBlocking();
                } catch (InterruptedException e) {
                    e.printStackTrace();
                }
            }
        }.start();

    }

    /**
     * 发送消息
     *
     * @param msg
     */
    public void sendMsg(String msg) {
        if (null != client) {
            System.out.println("JWebSocketClientService发送的消息：" + msg.length());
            client.send(msg);
        }
    }

    /**
     * 断开连接
     */
    private void closeConnect() {
        try {
            if (null != client) {
                client.close();
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            client = null;
        }
    }


